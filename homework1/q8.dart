/*8-What is the difference between the num and double data types in Dart?*/
void main() {
  // num can hold both integer and double values
  num number1 = 10; // integer value
  num number2 = 10.5; // double value

  // double can only hold double values
  double number3 = 15.75;

  print('Num values: $number1, $number2');
  print('Double value: $number3');
}
